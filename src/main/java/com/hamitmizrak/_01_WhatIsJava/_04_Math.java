package com.hamitmizrak._01_WhatIsJava;

public class _04_Math {
    public static void main(String[] args) {

        System.out.println(Math.min(2,100));
        System.out.println(Math.max(2,100));

        System.out.println(Math.sqrt(25));
        System.out.println(Math.abs(-15));
        System.out.println(Math.pow(5,3));

        System.out.println(Math.floor(9.9));
        System.out.println(Math.ceil(9.1));
        System.out.println(Math.round(9.4));
        System.out.println(Math.round(9.5));

        System.out.println(Math.sin(45));

    }
}
